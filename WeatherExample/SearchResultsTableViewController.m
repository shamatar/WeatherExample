- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return self.dataArray.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    CitySearchCell *cell = (CitySearchCell*)[tableView dequeueReusableCellWithIdentifier:@"CitySearchCell" forIndexPath:indexPath];
    cell.cityName.text=((City*)[self.dataArray objectAtIndex:indexPath.row]).nameAndCountry;
    return cell;
}
-(BOOL)tableView:(UITableView *)tableView shouldHighlightRowAtIndexPath:(NSIndexPath *)indexPath{
    return YES;
}
-(void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{
    [[NSNotificationCenter defaultCenter] postNotificationName:@"AddCity" object:[self.dataArray objectAtIndex:indexPath.row]];
}

